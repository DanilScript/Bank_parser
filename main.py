import pandas as pd
from pathlib import Path

# ============================
# ЧТЕНИЕ ВЫПИСКИ ИЗ CSV
# ============================

# путь к файлу выписки
file_path = Path("data/input/январь_26.csv")

print("Открываем файл:", file_path)

# читаем CSV файл
df = pd.read_csv(
    file_path,
    sep="\t",          # разделитель — табуляция
    encoding="cp1251", # кодировка (часто у банков)
    dtype=str          # читаем всё как строки
)

# ============================
# СОХРАНЕНИЕ В EXCEL
# ============================

# путь для сохранения
output_path = Path("data/output/январь_26_clean.xlsx")

# сохраняем в Excel (без служебного индекса)
df.to_excel(output_path, index=False)

print("\nФайл сохранён:", output_path)

# ============================
# ВАЖНО:
# ============================
